const std = @import("std");
const Io = std.Io;

pub const Operation = union(enum) {
    mov_imm: struct {
        rd: u4,
        imm: u32,
    },
    call_gba_div,
};

pub fn emitModule(writer: *Io.Writer, operations: []const Operation) Io.Writer.Error!void {
    try emitPrelude(writer);
    for (operations, 0..) |operation, index| {
        try emitOperation(writer, operation, index);
    }
    try emitPostlude(writer);
}

fn emitPrelude(writer: *Io.Writer) Io.Writer.Error!void {
    try writer.print("; generated by hmncli phase1 slice\n", .{});
    try writer.print("%GuestContext = type {{ [16 x i32] }}\n", .{});
    try writer.print("@.fmt = private unnamed_addr constant [4 x i8] c\"%d\\0A\\00\", align 1\n", .{});
    try writer.print("declare i32 @printf(ptr, ...)\n", .{});
    try writer.print("declare void @llvm.memset.p0.i64(ptr nocapture writeonly, i8, i64, i1 immarg)\n\n", .{});
    try writer.print("define i32 @shim_gba_Div(ptr %ctx) {{\n", .{});
    try writer.print("entry:\n", .{});
    try writer.print("  %regs = getelementptr inbounds %GuestContext, ptr %ctx, i32 0, i32 0\n", .{});
    try writer.print("  %r0_ptr = getelementptr inbounds [16 x i32], ptr %regs, i32 0, i32 0\n", .{});
    try writer.print("  %r1_ptr = getelementptr inbounds [16 x i32], ptr %regs, i32 0, i32 1\n", .{});
    try writer.print("  %numerator = load i32, ptr %r0_ptr, align 4\n", .{});
    try writer.print("  %denominator = load i32, ptr %r1_ptr, align 4\n", .{});
    try writer.print("  %result = sdiv i32 %numerator, %denominator\n", .{});
    try writer.print("  store i32 %result, ptr %r0_ptr, align 4\n", .{});
    try writer.print("  ret i32 %result\n", .{});
    try writer.print("}}\n\n", .{});
    try writer.print("define i32 @main() {{\n", .{});
    try writer.print("entry:\n", .{});
    try writer.print("  %ctx = alloca %GuestContext, align 4\n", .{});
    try writer.print("  %regs = getelementptr inbounds %GuestContext, ptr %ctx, i32 0, i32 0\n", .{});
    try writer.print("  call void @llvm.memset.p0.i64(ptr align 4 %regs, i8 0, i64 64, i1 false)\n", .{});
}

fn emitOperation(writer: *Io.Writer, operation: Operation, index: usize) Io.Writer.Error!void {
    switch (operation) {
        .mov_imm => |mov| {
            try writer.print(
                "  %r{d}_ptr_{d} = getelementptr inbounds [16 x i32], ptr %regs, i32 0, i32 {d}\n",
                .{ mov.rd, index, mov.rd },
            );
            try writer.print("  store i32 {d}, ptr %r{d}_ptr_{d}, align 4\n", .{
                mov.imm,
                mov.rd,
                index,
            });
        },
        .call_gba_div => {
            try writer.print("  %div_{d} = call i32 @shim_gba_Div(ptr %ctx)\n", .{index});
        },
    }
}

fn emitPostlude(writer: *Io.Writer) Io.Writer.Error!void {
    try writer.print("  %r0_ptr_done = getelementptr inbounds [16 x i32], ptr %regs, i32 0, i32 0\n", .{});
    try writer.print("  %result = load i32, ptr %r0_ptr_done, align 4\n", .{});
    try writer.print("  %print = call i32 (ptr, ...) @printf(ptr @.fmt, i32 %result)\n", .{});
    try writer.print("  ret i32 0\n", .{});
    try writer.print("}}\n", .{});
}

test "llvm emission includes the gba div shim and result print" {
    var output: Io.Writer.Allocating = .init(std.testing.allocator);
    defer output.deinit();

    try emitModule(&output.writer, &.{
        .{ .mov_imm = .{ .rd = 0, .imm = 10 } },
        .{ .mov_imm = .{ .rd = 1, .imm = 2 } },
        .call_gba_div,
    });

    try std.testing.expectStringStartsWith(output.writer.buffered(), "; generated by hmncli phase1 slice\n");
    try std.testing.expect(std.mem.indexOf(u8, output.writer.buffered(), "@shim_gba_Div") != null);
}
