    try writer.print("  call void @llvm.memset.p0.i64(ptr align 1 %state_vram_ptr, i8 0, i64 98304, i1 false)\n", .{});
    try writer.print("  store i1 true, ptr %state_dispstat_toggle_ptr, align 1\n", .{});
